import { existsSync, readFileSync, readdirSync } from 'node:fs';
import { resolve } from 'node:path';

const projectRoot = process.cwd();
const staticRoot = resolve(projectRoot, '.vercel', 'output', 'static');
const vercelRoot = resolve(projectRoot, '.vercel', 'output');
const clientManifestPath = resolve(projectRoot, '.svelte-kit', 'output', 'client', '.vite', 'manifest.json');

function walk(dir) {
  const files = [];

  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = resolve(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(fullPath));
    } else {
      files.push(fullPath);
    }
  }

  return files;
}

function assertExists(path, label) {
  if (!existsSync(path)) {
    throw new Error(`Missing ${label}: ${path}`);
  }
}

assertExists(staticRoot, 'static build output');
assertExists(vercelRoot, 'Vercel output');

for (const file of [
  'favicon.svg',
  'social-card.svg',
  'tutorial-1.jpg',
  'tutorial-2.png',
  'tutorial-3.png',
  'tutorials/ai-studio/step-1.jpg',
  'tutorials/ai-studio/step-2.png',
  'tutorials/ai-studio/step-3.png',
  '_app/version.json'
]) {
  assertExists(resolve(staticRoot, file), `static asset ${file}`);
}

for (const file of [
  'config.json',
  'functions/![-]/catchall.func/.vc-config.json',
  'functions/index.func/.vc-config.json',
  'functions/dashboard.func/.vc-config.json'
]) {
  assertExists(resolve(vercelRoot, file), `Vercel artifact ${file}`);
}

const routeConfig = JSON.parse(readFileSync(resolve(vercelRoot, 'config.json'), 'utf8'));
const expectedDests = [
  '/',
  '/catalog',
  '/characters/[slug]',
  '/dashboard',
  '/dashboard/login',
  '/dashboard/logout',
  '/dashboard/prompts',
  '/dashboard/prompts/[id]',
  '/dashboard/site/[document]',
  '/faq',
  '/learn/ai-studio',
  '/vault',
  '/![-]/catchall'
];

for (const dest of expectedDests) {
  if (!routeConfig.routes.some((route) => route.dest === dest)) {
    throw new Error(`Missing expected Vercel route destination: ${dest}`);
  }
}

const forbiddenFragments = [
  '// AUTO-GENERATED BY scripts/generate-character-routes.mjs',
  '@DECODER{CPF v1',
  'SOUL_ENGINE v1.0 | CHARACTER:'
];

for (const filePath of walk(staticRoot)) {
  if (!/\.(js|css|svg|html|json)$/.test(filePath)) {
    continue;
  }

  const contents = readFileSync(filePath, 'utf8');
  for (const fragment of forbiddenFragments) {
    if (contents.includes(fragment)) {
      throw new Error(`Forbidden client build fragment "${fragment}" found in ${filePath}`);
    }
  }
}

if (existsSync(clientManifestPath)) {
  const manifest = JSON.parse(readFileSync(clientManifestPath, 'utf8'));
  const expectedAssets = new Set(
    Object.values(manifest)
      .flatMap((entry) => [entry.file, ...(entry.css ?? [])])
      .map((asset) => resolve(staticRoot, asset))
  );
  const immutableRoot = resolve(staticRoot, '_app', 'immutable');

  if (existsSync(immutableRoot)) {
    for (const filePath of walk(immutableRoot)) {
      if (!/\.(js|css)$/.test(filePath)) {
        continue;
      }

      if (!expectedAssets.has(filePath)) {
        throw new Error(`Stale immutable asset found in build output: ${filePath}`);
      }
    }
  }
}

console.log('Build output looks correct.');
