import { mkdir, readdir, readFile, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  getAllCharacters,
  meghaUniverse
} from '../src/lib/content/characters.ts';
import { getRelatedCharacters } from '../src/lib/utils/characterPage.ts';
import { getPromptTeaser } from '../src/lib/utils/promptSummary.ts';

const GENERATED_MARKER = '// AUTO-GENERATED BY scripts/generate-character-routes.mjs';

const GENERATED_TEMPLATE_COMMENT = '<!-- AUTO-GENERATED BY scripts/generate-character-routes.mjs -->';

const routePage = `${GENERATED_TEMPLATE_COMMENT}
<script lang="ts">
  import CharacterDetailPage from '$lib/components/CharacterDetailPage.svelte';
  import type { PageData } from './$types';

  export let data: PageData;
</script>

<CharacterDetailPage {data} />
`;

function getRelatedListings(character, allCharacters) {
  return getRelatedCharacters(character, allCharacters).map(toCharacterListing);
}

function routeLoad(character, allCharacters) {
  const related = getRelatedListings(character, allCharacters);

  return `${GENERATED_MARKER}
import type { CharacterDefinition, CharacterListing } from '$lib/content/types';

const character: CharacterDefinition = ${JSON.stringify(character, null, 2)};

const related: CharacterListing[] = ${JSON.stringify(related, null, 2)};

export const prerender = true;

export function load() {
  return {
    character,
    related
  };
}
`;
}

function toCharacterListing(character) {
  return {
    id: character.id,
    slug: character.slug,
    name: character.name,
    tagline: character.tagline,
    tone: character.tone,
    tier: character.tier,
    contentRating: character.contentRating,
    ...(character.nsfwTier ? { nsfwTier: character.nsfwTier } : {}),
    region: character.region,
    archetype: character.archetype,
    price: character.price,
    teaser: getPromptTeaser(character)
  };
}

function listingsModule() {
  const allCharacterListings = getAllCharacters().map(toCharacterListing);
  const meghaUniverseIds = meghaUniverse.map((character) => character.id);

  return `${GENERATED_MARKER}
import type { CharacterListing } from './types';

export const allCharacterListings: CharacterListing[] = ${JSON.stringify(allCharacterListings, null, 2)};

export const meghaUniverseIds = ${JSON.stringify(meghaUniverseIds, null, 2)};
`;
}

async function writeIfChanged(filePath, contents) {
  let current = null;

  try {
    current = await readFile(filePath, 'utf8');
  } catch {
    current = null;
  }

  if (current !== contents) {
    await writeFile(filePath, contents, 'utf8');
  }
}

async function main() {
  const rootDir = path.dirname(fileURLToPath(import.meta.url));
  const projectRoot = path.resolve(rootDir, '..');
  const routesRoot = path.join(projectRoot, 'src', 'routes', 'characters');
  const listingsModulePath = path.join(
    projectRoot,
    'src',
    'lib',
    'content',
    'character-listings.generated.ts'
  );
  const allCharacters = getAllCharacters();
  const slugs = new Set(allCharacters.map((character) => character.slug));

  const existingEntries = await readdir(routesRoot, { withFileTypes: true });

  for (const entry of existingEntries) {
    if (!entry.isDirectory() || entry.name === '[slug]' || slugs.has(entry.name)) {
      continue;
    }

    const markerPath = path.join(routesRoot, entry.name, '+page.ts');

    try {
      const markerFile = await readFile(markerPath, 'utf8');
      if (markerFile.startsWith(GENERATED_MARKER)) {
        await rm(path.join(routesRoot, entry.name), { recursive: true, force: true });
      }
    } catch {
      // Ignore non-generated directories.
    }
  }

  for (const character of allCharacters) {
    const routeDir = path.join(routesRoot, character.slug);
    await mkdir(routeDir, { recursive: true });
    await writeIfChanged(path.join(routeDir, '+page.ts'), routeLoad(character, allCharacters));
    await writeIfChanged(path.join(routeDir, '+page.svelte'), routePage);
  }

  await writeIfChanged(listingsModulePath, listingsModule());
}

await main();
